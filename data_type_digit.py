# 숫자형의 종류 - integer(정수 - 소수점이 없는 숫자), floating point(실수 - 소수점이 있는 숫자)

print(type(0))
print(type(0.0))

# 숫자 연산자 
# 사칙 연산자 - +, - ,  * , /, //, %

dit_a = 7
dig_b = 2

print("dit_a + dig_b = 의 더하기는", dit_a + dig_b)
print("dit_a - dig_b = 의 빼기는", dit_a - dig_b)
print("dit_a * dig_b = 의 곱셈은", dit_a * dig_b)
print("dit_a / dig_b = 의 나누기는", dit_a / dig_b, type(dit_a / dig_b))
print("dit_a // dig_b = 의 정수 나누기는", dit_a // dig_b, type(dit_a // dig_b))
print("dit_a % dig_b = 의 나머지는", dit_a % dig_b, type(dit_a % dig_b))

# 제곱 연산자 **

print(dit_a ** 2)


